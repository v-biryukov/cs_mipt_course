/*
    Оператор нахождение остатка %

    Для нахождения остатка деления двух чисел используется оператор %

    Например, при делении 20 на 7 результат будет равен 2 и 6 в остатке
*/

#include <stdio.h>

int main()
{
    int a = 20;
    int b = 7;

    printf("%i\n", a / b);
    printf("%i\n", a % b);
}

