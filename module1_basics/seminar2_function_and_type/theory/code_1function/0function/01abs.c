/*
    Функция abs

    В качестве примера функции рассмотрим функцию  abs  из библиотеки stdlib.
    Эта функция принимает число типо int и возвращает абсолютное значение (модуль) этого числа.

*/

#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a = abs(-10);
    printf("%i\n", a);

    printf("%i\n", abs(-20));
}


